package javaBasicConcepts;

public class Loops {
    public static void main(String[] args) {

        //while loop
        int i = 0;
        while(i <5){
            System.out.println(i);
            i++;
        }
        // for loop
        for(int j=0; j<10; j++){
            System.out.println(j);
        }

        // do-while loop
        int k = 0;
        do{
            System.out.println(k);
            k++;
        } while(k < 3);

    }
}
